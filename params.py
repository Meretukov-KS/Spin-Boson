"""
params.py — параметры модели
=============================
Структура:
  1. Общие параметры        — используются и в TCL2, и в HEOM
  2. Параметры только TCL2  — спектральная плотность J(ω,t), окружение
  3. Параметры только HEOM  — иерархия, разложение Матсубары/Паде
"""

# ══════════════════════════════════════════════════════════════════════
# 1. ОБЩИЕ ПАРАМЕТРЫ
#    Используются одинаково в обоих методах
# ══════════════════════════════════════════════════════════════════════

# --- Спиновая система ---
DELTA    = 0.5    # туннельная амплитуда (σ_x)
H_FIELD  = 0.5    # продольное поле (σ_z)

# --- Начальное состояние ---
# Вектор Блоха (нормируется автоматически внутри каждого модуля)
PAULI_I  = [0, 0, 1.0]

# --- Параметры лоренцевской бани (общие для J в HEOM и TCL2 compare-режиме) ---
lambda_s = 0.05   # амплитуда спектральной плотности: J(ω) = lambda_s·γ·ω/(ω²+γ²)
GAMMA    = 0.5    # ширина лоренцевского пика (γ)
TEMP     = 200.0    # температура (k_B·T в единицах ħ·Δ)

# --- Временная сетка ---
T_MAX    = 80.0   # время интегрирования
T_POINTS = 2000   # число точек по времени

# ══════════════════════════════════════════════════════════════════════
# 2. ПАРАМЕТРЫ ТОЛЬКО TCL2
#    Определяют произвольную J(ω,t) и начальное заполнение мод N_I(ω).
#    В режиме compare эти параметры игнорируются — run.py подставляет
#    лоренцевскую J и тепловое N_I, совместимые с HEOM.
# ══════════════════════════════════════════════════════════════════════

# Общий множитель связи для TCL2 (входит как λ² в интеграл диссипатора)
LAMBDA       = 10 ** (-1)

# Сетка по частоте для интегрирования по окружению
OMEGA_MAX    = 40.0   # верхняя граница ∫dω
OMEGA_POINTS = 1000   # число точек сетки по ω

# --- Омическая часть J ---
eta  = 0.2    # амплитуда (η)
s    = 1.0    # показатель степени (s=1 — омическая, s<1 — суб-, s>1 — сверх-)
wc   = 4.0    # частота обрезания (ω_c)

# --- Лоренцевская (резонансная) часть J ---
k    = 1.0    # ширина лоренцевского пика (κ)
g    = 1.5    # амплитуда связи (g)
w0   = 10.0   # центральная частота резонанса (ω₀)
df   = 0.05   # амплитуда осцилляций центральной частоты

# ══════════════════════════════════════════════════════════════════════
# 3. ПАРАМЕТРЫ ТОЛЬКО HEOM
#    Управляют точностью метода иерархических уравнений движения.
#    Используют lambda_s, GAMMA, TEMP из раздела 1.
# ══════════════════════════════════════════════════════════════════════

N_DEPTH     = 3     # глубина иерархии (чем больше — тем точнее, но медленнее)
N_MATSUBARA = 3     # число членов разложения (Матсубара или Паде)
USE_PADE    = True  # True — разложение Паде (точнее при малом N_MATSUBARA)
                    # False — стандартное разложение Матсубары